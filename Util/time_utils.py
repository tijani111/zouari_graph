import time

def time_measure(func):
    def wrapper(*args, **kwargs):
        start_time = time.time()
        result = func(*args, **kwargs)
        end_time = time.time()
        print(f"{func.__name__} ausgeführt in: {end_time - start_time} Sekunden")
        return result
    return wrapper