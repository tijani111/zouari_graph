 " Vermittlungsweise abhängt. Für Studierende, die in Richtung Forschung, Compilerbau, formale Verifikation"
 " oder Sicherheit gehen, ist das Modul unverzichtbar; für andere bleibt der Nutzen oft abstrakt. Didaktische"
