"Curriculum-Gap-Analysen zeigen, dass Hochschulen oft Schwierigkeiten haben, moderne Technologien zeitnah in die Lehre zu integrieren, was zu einem zeitlichen Verzug gegenüber der Praxis führt ."
"\n\n"
"Ein Try ist die systematische Integration solcher Themen über Querschnittsmodule, technologiebezogene Praktika oder regelmäßig aktualisierte „Special Topics“-Veranstaltungen."
"\n\n"
"So könnten Grundlagen zu Cloud-Architekturen und DevOps-Pipelines in Betriebssystem-, Software-Engineering- und Datenbankmodulen aufgegriffen werden, während Security-Aspekte in Programmierung, Netzen und Software-Engineering verankert werden."
"\n\n"
"KI-Grundlagen sollten nicht nur als spezialisierter Wahlbereich, sondern als Basiskomponente digitaler Kompetenz verstanden werden."
